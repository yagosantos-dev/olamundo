print('usado como base que 1L de tinta pinte uma area de 2 metros ')
altura = float(input("qual a altura da parede "))

largura = float(input("qual a largura da parede  "))

area = altura * largura

tinta = area / 2

print(f"sua parede tem a dimenção de {altura}x{largura} e tem a area de {area}.\npara pintar essa parede , voçe vai usar  {tinta}L de tinta ")